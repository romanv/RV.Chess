        private static readonly Dictionary<PieceType, int> PieceIndices = new()
        {
            { PieceType.Pawn, 0 },
            { PieceType.Knight, 1 },
            { PieceType.Bishop, 2 },
            { PieceType.Rook, 3 },
            { PieceType.Queen, 4 },
            { PieceType.King, 5 },
        };

        public static uint ZobristHash(this Chessgame game)
        {
            return HashPieces(game.Board) ^ HashCastling(game.CastlingRights) ^ HashTurn(game) ^ HashEnPassant(game);
        }

        private static uint HashPieces(Chessboard board)
        {
            uint hash = 0;
            var pieces = board.OccupiedBoard;

            while (pieces > 0)
            {
                var sourceSquare = pieces.LastSignificantBitIndex();
                var type = board.GetPieceTypeAt(sourceSquare);
                var side = board.GetPieceSideAt(sourceSquare);
                hash ^= HashPiece(sourceSquare, type, side);
                pieces &= ~(1UL << sourceSquare);
            }

            return hash;
        }

        private static uint HashPiece(int square, PieceType type, Side side)
        {
            var index = PieceIndices[type] * 2 + (side == Side.White ? 1 : 0);
            return PIECES[64 * index + square];
        }

        private static uint HashCastling(CastlingRights cr)
        {
            uint hash = 0;

            if (cr.Rights.HasFlag(CastlingDirection.WhiteKingside)) hash ^= CASTLING[0];
            if (cr.Rights.HasFlag(CastlingDirection.WhiteQueenside)) hash ^= CASTLING[1];
            if (cr.Rights.HasFlag(CastlingDirection.BlackKingside)) hash ^= CASTLING[2];
            if (cr.Rights.HasFlag(CastlingDirection.BlackQueenside)) hash ^= CASTLING[3];

            return hash;
        }

        private static uint HashTurn(Chessgame game)
        {
            return game.SideToMove == Side.White ? WHITE_TURN : 0;
        }

        private static uint HashEnPassant(Chessgame game)
        {
            return game.EnPassantSquareIdx > -1
                ? EN_PASSANT[game.EnPassantSquareIdx % 8]
                : 0;
        }

